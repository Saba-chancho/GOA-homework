# 1)  მომხმარებელს შემოატანინეთ თავისი გულის ცემა გააკეთეთ პატარა გულის ცემის სისტემა რომელიც ითვლის
# რა თქმა უნდა გულის ცემას  თუ 180 ზე მეტია ამ შემთხვევაში მაღალია თუ 160-დან 170 მდე არის ამ შემთვევაში 
# არის ნორმალური თუ 160-ზე ნაკლებია ამ შემთხვევაში დაბალია 


heart_rate = int(input("შეიყვანეთ თქვენი წნევა: "))
if heart_rate >= 180 :
    print("გულის ცემა მაღალია")
elif heart_rate >= 160 :
    print("გულის წნევა ნორმალურია")
else:
    print("გულის წნევა დაბალია")